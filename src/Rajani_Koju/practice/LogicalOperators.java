package practice;

public class LogicalOperators {

	public static void main(String[] args) {
		boolean a = false;
		boolean b = true;
		
		// && operator
		
		System.out.println("a && b : " + (a && b));
		
		// || operator
		
		System.out.println("a || b : " +(a || b));
		
		// ! operator
		System.out.println("!(a&&b) :" +!(a&&b));



	}

}
